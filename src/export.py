    def export_gdmt_report(
        self,
        gdmt_recommendation: GDMTRecommendation,
        patient_id: str = "DEMO",
    ) -> str:
        """Export a GDMT optimisation report.

        Produces a detailed Markdown report covering the four pillars of
        guideline-directed medical therapy for heart failure, including
        current status, gaps, titration plans, and device therapy
        considerations.

        Args:
            gdmt_recommendation: A ``GDMTRecommendation`` instance.
            patient_id: Patient identifier.

        Returns:
            Markdown report as a string.
        """
        gdmt = gdmt_recommendation
        timestamp = _now_display()
        template = REPORT_TEMPLATES["gdmt_optimization"]
        lines: List[str] = [
            template.format(
                patient_id=patient_id, timestamp=timestamp, version=VERSION,
            )
        ]

        # Patient classification
        lines.append("## Heart Failure Classification\n")
        lines.append(f"- **EF Category:** {gdmt.ef_category.value}")
        lines.append(f"- **NYHA Class:** {gdmt.nyha_class.value}")
        lines.append(
            f"- **Overall Status:** {gdmt.overall_status or 'Not assessed'}"
        )
        sev_label = gdmt.severity.value.replace("_", " ").title()
        lines.append(f"- **Severity:** {sev_label}\n")

        # Four pillars status
        lines.append("## GDMT Four Pillars Status\n")
        lines.append(
            "| Pillar | Drug | Current Dose | Target Dose | Status |"
        )
        lines.append(
            "|--------|------|-------------|-------------|--------|"
        )
        for pillar_name, med in gdmt.current_gdmt.items():
            status_label = med.status.value.replace("_", " ").title()
            lines.append(
                f"| {pillar_name.replace('_', ' ').title()} "
                f"| {med.drug_name} "
                f"| {med.current_dose or 'N/A'} "
                f"| {med.target_dose or 'N/A'} "
                f"| {status_label} |"
            )
        lines.append("")

        # Gaps identified
        if gdmt.gaps_identified:
            lines.append("## Gaps Identified\n")
            for gap in gdmt.gaps_identified:
                lines.append(f"- {gap}")
            lines.append("")

        # New medications to initiate
        if gdmt.new_medications:
            lines.append("## New Medications to Initiate\n")
            for med in gdmt.new_medications:
                if isinstance(med, dict):
                    name = med.get("drug", med.get("name", "Unknown"))
                    dose = med.get("starting_dose", med.get("dose", ""))
                    reason = med.get("reason", "")
                    lines.append(f"- **{name}**: Start {dose}")
                    if reason:
                        lines.append(f"  - Rationale: {reason}")
                else:
                    lines.append(f"- {med}")
            lines.append("")

        # Titration plans
        if gdmt.titration_plans:
            lines.append("## Titration Plans\n")
            for plan in gdmt.titration_plans:
                if isinstance(plan, dict):
                    drug = plan.get("drug", "Unknown")
                    current = plan.get("current_dose", "")
                    target = plan.get("target_dose", "")
                    interval = plan.get("titration_interval", "2 weeks")
                    lines.append(
                        f"- **{drug}**: {current} -> {target} "
                        f"(titrate every {interval})"
                    )
                else:
                    lines.append(f"- {plan}")
            lines.append("")

        # Additional therapies
        if gdmt.additional_therapies:
            lines.append("## Additional Therapies\n")
            for therapy in gdmt.additional_therapies:
                if isinstance(therapy, dict):
                    name = therapy.get("name", therapy.get("therapy", "Unknown"))
                    indication = therapy.get("indication", "")
                    lines.append(f"- **{name}**")
                    if indication:
                        lines.append(f"  - Indication: {indication}")
                else:
                    lines.append(f"- {therapy}")
            lines.append("")

        # Device therapy
        if gdmt.device_therapy:
            lines.append("## Device Therapy Considerations\n")
            for device in gdmt.device_therapy:
                if isinstance(device, dict):
                    dtype = device.get("type", device.get("device", "Unknown"))
                    indication = device.get("indication", "")
                    criteria = device.get("criteria_met", "")
                    lines.append(f"- **{dtype}**")
                    if indication:
                        lines.append(f"  - Indication: {indication}")
                    if criteria:
                        lines.append(f"  - Criteria: {criteria}")
                else:
                    lines.append(f"- {device}")
            lines.append("")

        # Monitoring plan
        if gdmt.monitoring_plan:
            lines.append("## Monitoring Plan\n")
            for key, value in gdmt.monitoring_plan.items():
                label = key.replace("_", " ").title()
                lines.append(f"- **{label}:** {value}")
            lines.append("")

        # Contraindications and warnings
        if gdmt.contraindication_notes:
            lines.append("## Contraindication Notes\n")
            for note in gdmt.contraindication_notes:
                lines.append(f"- {note}")
            lines.append("")

        if gdmt.interaction_warnings:
            lines.append("## Drug Interaction Warnings\n")
            for warning in gdmt.interaction_warnings:
                lines.append(f"- {warning}")
            lines.append("")

        # Reassessment
        lines.append("## Reassessment Timeline\n")
        lines.append(f"{gdmt.reassessment_timeline}\n")

        # Guideline reference
        lines.append(format_guideline_citation(gdmt.guideline_reference))

        lines.append(_DISCLAIMER)
        return "\n".join(lines)

