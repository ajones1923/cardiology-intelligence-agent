    # ── Risk Score Report ──────────────────────────────────────────────

    def export_risk_score_report(
        self,
        risk_scores: List[RiskScoreResult],
        patient_id: str = "DEMO",
    ) -> str:
        """Export a dedicated risk score report with all calculated scores.

        Produces a detailed Markdown report focused exclusively on
        cardiovascular risk scores, their interpretation, component
        breakdowns, and clinical recommendations.

        Args:
            risk_scores: List of ``RiskScoreResult`` instances.
            patient_id: Patient identifier.

        Returns:
            Markdown report as a string.
        """
        timestamp = _now_display()
        template = REPORT_TEMPLATES["risk_assessment"]
        lines: List[str] = [
            template.format(
                patient_id=patient_id, timestamp=timestamp, version=VERSION,
            )
        ]

        if not risk_scores:
            lines.append("*No risk scores available for this patient.*\n")
            lines.append(_DISCLAIMER)
            return "\n".join(lines)

        # Overall severity (highest among scores)
        max_sev = max(risk_scores, key=lambda r: _severity_ordinal(r.severity))
        lines.append(
            f"**Overall Risk Severity:** "
            f"{_severity_badge(max_sev.severity)}\n\n"
        )

        # Summary table
        lines.append("## Score Summary\n")
        lines.append(format_risk_score_table(risk_scores))
        lines.append("\n")

        # Detailed breakdown per score
        lines.append("## Detailed Score Analysis\n")
        for i, rs in enumerate(risk_scores, 1):
            stype = rs.score_type.value.upper().replace("_", "-")
            lines.append(f"### {i}. {stype}\n")
            lines.append(f"- **Score Value:** {rs.score_value:.1f}")
            lines.append(f"- **Risk Category:** {rs.risk_category}")
            sev_label = rs.severity.value.replace("_", " ").title()
            lines.append(f"- **Severity:** {sev_label}")
            lines.append(f"- **Interpretation:** {rs.interpretation}\n")

            class_loe = _format_class_loe(rs.guideline_class, rs.evidence_level)
            if class_loe:
                lines.append(f"**Guideline Recommendation:** {class_loe}\n")
            if rs.guideline_reference:
                lines.append(format_guideline_citation(rs.guideline_reference))

            if rs.component_breakdown:
                lines.append("\n**Component Breakdown:**\n")
                lines.append("| Component | Value |")
                lines.append("|-----------|-------|")
                for comp_name, comp_val in rs.component_breakdown.items():
                    lines.append(f"| {comp_name} | {comp_val} |")
                lines.append("")

            if rs.recommendations:
                lines.append("\n**Clinical Recommendations:**\n")
                for rec in rs.recommendations:
                    lines.append(f"- {rec}")
                lines.append("")

            lines.append("")

        # Aggregated recommendations
        all_recs = []
        for rs in risk_scores:
            all_recs.extend(rs.recommendations)
        if all_recs:
            lines.append("## Consolidated Recommendations\n")
            seen = set()
            for rec in all_recs:
                if rec not in seen:
                    seen.add(rec)
                    lines.append(f"- {rec}")
            lines.append("\n")

        lines.append(_DISCLAIMER)
        return "\n".join(lines)

