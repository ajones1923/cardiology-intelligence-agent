def _severity_ordinal(severity: SeverityLevel) -> int:
    """Map severity to an ordinal for comparison."""
    order = {
        SeverityLevel.LOW: 0,
        SeverityLevel.BORDERLINE: 1,
        SeverityLevel.INTERMEDIATE: 2,
        SeverityLevel.MODERATE: 3,
        SeverityLevel.HIGH: 4,
        SeverityLevel.VERY_HIGH: 5,
        SeverityLevel.CRITICAL: 6,
    }
    return order.get(severity, 0)


def _fhir_interpretation_code(risk_category: str) -> str:
    """Map a risk category string to a FHIR observation interpretation code.

    Uses the v3 ObservationInterpretation value set:
      - N  = Normal
      - L  = Low
      - H  = High
      - HH = Critical high
    """
    cat = risk_category.lower()
    if "critical" in cat or "very high" in cat:
        return "HH"
    if "high" in cat:
        return "H"
    if "low" in cat:
        return "L"
    return "N"


