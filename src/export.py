def _now_iso() -> str:
    """Return the current UTC timestamp in ISO-8601 format."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _now_display() -> str:
    """Return a human-friendly UTC timestamp."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


def _severity_badge(severity: SeverityLevel) -> str:
    """Return a Markdown badge string for a severity level."""
    colour = SEVERITY_COLORS.get(severity, "#95A5A6")
    label = severity.value.replace("_", " ").title()
    return f"![{label}](https://img.shields.io/badge/Severity-{label.replace(' ', '%20')}-{colour[1:]})"


def _severity_emoji(severity: SeverityLevel) -> str:
    """Return a text indicator for severity (no actual emoji, just text markers)."""
    mapping = {
        SeverityLevel.LOW: "[LOW]",
        SeverityLevel.BORDERLINE: "[BORDERLINE]",
        SeverityLevel.INTERMEDIATE: "[INTERMEDIATE]",
        SeverityLevel.MODERATE: "[MODERATE]",
        SeverityLevel.HIGH: "[HIGH]",
        SeverityLevel.VERY_HIGH: "[VERY HIGH]",
        SeverityLevel.CRITICAL: "[CRITICAL]",
    }
    return mapping.get(severity, "[UNKNOWN]")


def format_risk_score_table(scores: List[RiskScoreResult]) -> str:
    """Format a list of risk scores as a Markdown table.

    Args:
        scores: List of ``RiskScoreResult`` instances.

    Returns:
        Markdown table string with columns for score type, value,
        category, severity, and interpretation.
    """
    if not scores:
        return "*No risk scores calculated.*\n"

    lines = [
        "| Score | Value | Risk Category | Severity | Interpretation |",
        "|-------|-------|---------------|----------|----------------|",
    ]
    for s in scores:
        stype = s.score_type.value.upper().replace("_", "-")
        severity_label = s.severity.value.replace("_", " ").title()
        interp = s.interpretation[:80] if s.interpretation else ""
        lines.append(
            f"| {stype} | {s.score_value:.1f} | {s.risk_category} "
            f"| {severity_label} | {interp} |"
        )
    return "\n".join(lines) + "\n"


def format_guideline_citation(guideline: str) -> str:
    """Format a guideline reference as a Markdown citation block.

    Args:
        guideline: Guideline reference string (e.g. ``"ACC/AHA 2022 ..."``).

    Returns:
        Markdown blockquote citation.
    """
    if not guideline:
        return ""
    return f"> **Guideline:** {guideline}\n"


def _format_class_loe(
    guideline_class: Optional[GuidelineClass],
    evidence_level: Optional[EvidenceLevel],
) -> str:
    """Format guideline Class and Level of Evidence as a compact string."""
    parts = []
    if guideline_class:
        parts.append(f"Class {guideline_class.value}")
    if evidence_level:
        parts.append(f"LOE {evidence_level.value}")
    return " / ".join(parts) if parts else ""


def _format_cross_modal_trigger(trigger: CrossModalTrigger) -> str:
    """Format a single cross-modal trigger as Markdown."""
    lines = [
        f"- **{trigger.trigger_type.title()} Trigger:** {trigger.finding}",
    ]
    if trigger.conditions:
        lines.append(f"  - Suspected conditions: {', '.join(trigger.conditions)}")
    if trigger.gene_panel:
        lines.append(f"  - Recommended gene panel: {', '.join(trigger.gene_panel)}")
    if trigger.criteria_met:
        lines.append(f"  - Criteria met: {trigger.criteria_met}")
    if trigger.guideline:
        lines.append(f"  - Guideline: {trigger.guideline}")
    urgency = trigger.urgency.value.replace("_", " ").title()
    lines.append(f"  - Urgency: {urgency}")
    return "\n".join(lines)


def _generate_filename(prefix: str, extension: str) -> str:
    """Generate a timestamped filename for export."""
    ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    return f"{prefix}_{ts}.{extension}"


