    def export_workflow_report(
        self,
        workflow_result: WorkflowResult,
        patient_id: str = "DEMO",
    ) -> str:
        """Export clinical workflow results as a structured report.

        This is a convenience wrapper around ``export_markdown()`` with
        the ``workflow`` template and additional raw-data sections.

        Args:
            workflow_result: A ``WorkflowResult`` instance.
            patient_id: Patient identifier.

        Returns:
            Markdown report as a string.
        """
        timestamp = _now_display()
        wf_type = workflow_result.workflow_type.value.replace("_", " ").title()
        template = REPORT_TEMPLATES["workflow"]
        lines: List[str] = [
            template.format(
                patient_id=patient_id,
                timestamp=timestamp,
                version=VERSION,
                workflow_type=wf_type,
            )
        ]

        # Severity and confidence
        lines.append(
            f"**Severity:** {_severity_badge(workflow_result.severity)}\n"
        )
        lines.append(f"**Confidence:** {workflow_result.confidence:.0%}\n\n")

        # Summary
        lines.append("## Summary\n")
        if workflow_result.summary:
            lines.append(f"{workflow_result.summary}\n\n")
        else:
            lines.append("*No summary available.*\n\n")

        # Findings
        if workflow_result.findings:
            lines.append("## Findings\n")
            for i, f in enumerate(workflow_result.findings, 1):
                lines.append(f"{i}. {f}")
            lines.append("\n")

        # Risk scores
        if workflow_result.risk_scores:
            lines.append("## Risk Scores\n")
            lines.append(format_risk_score_table(workflow_result.risk_scores))
            lines.append("\n")

        # Recommendations
        if workflow_result.recommendations:
            lines.append("## Recommendations\n")
            for i, rec in enumerate(workflow_result.recommendations, 1):
                lines.append(f"{i}. {rec}")
            lines.append("\n")

        # Guideline references
        if workflow_result.guideline_references:
            lines.append("## Guideline References\n")
            for ref in workflow_result.guideline_references:
                lines.append(format_guideline_citation(ref))
            lines.append("\n")

        # Cross-modal triggers
        if workflow_result.cross_modal_triggers:
            lines.append("## Cross-Modal Genomic Triggers\n")
            for trigger in workflow_result.cross_modal_triggers:
                if isinstance(trigger, CrossModalTrigger):
                    lines.append(_format_cross_modal_trigger(trigger))
                lines.append("")
            lines.append("\n")

        # Raw data (if present)
        if workflow_result.raw_data:
            lines.append("## Intermediate Data\n")
            lines.append("```json")
            lines.append(json.dumps(workflow_result.raw_data, indent=2, default=str))
            lines.append("```\n")

        lines.append(_DISCLAIMER)
        return "\n".join(lines)


